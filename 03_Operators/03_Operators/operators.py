# Arithmetic Operators
a = 10
b = 3
print("Addition:", a + b)
print("Division:", a / b)
print("Floor Division:", a // b)
print("Power:", a ** b)

# Comparison Operators
print("a > b:", a > b)
print("a == 10:", a == 10)

# Logical Operators
x = True
y = False
print("x and y:", x and y)
print("x or y:", x or y)
print("not x:", not x)

# Assignment Operators
a += 5
print("a after += 5:", a)
