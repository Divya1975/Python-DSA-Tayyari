# Numeric Types
x = 10
y = 3.14
z = 3 + 2j

print("Integer:", x)
print("Float:", y)
print("Complex:", z)

# String
name = "Divya"
print("String:", name)

# List
numbers = [1, 2, 3, 4]
print("List:", numbers)

# Tuple
coordinates = (10, 20)
print("Tuple:", coordinates)

# Set
unique = {1, 2, 2, 3}
print("Set (Unique):", unique)

# Dictionary
student = {"name": "Divya", "age": 20}
print("Dictionary:", student)

# Boolean
is_valid = True
print("Boolean:", is_valid)

# Type Casting
a = int("5")
b = float("4.5")
c = str(100)
print("After casting:", a, b, c)
